public class MachineFin {
    int numeroMachine;
    int tempsUtilisation;

    public MachineFin(int numeroMachine, int tempsUtilisation) {
        this.numeroMachine = numeroMachine;
        this.tempsUtilisation = tempsUtilisation;
    }

    public int getNumeroMachine() {
        return numeroMachine;
    }

    public void setNumeroMachine(int numeroMachine) {
        this.numeroMachine = numeroMachine;
    }

    public int getTempsUtilisation() {
        return tempsUtilisation;
    }

    public void setTempsUtilisation(int tempsUtilisation) {
        this.tempsUtilisation = tempsUtilisation;
    }
}
